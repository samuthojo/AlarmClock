    //Background task: monitoring the time
    private class AlarmTask extends SwingWorker<Void, Void> {

        @Override
        public Void doInBackground() {
            DateFormat k;

            k = DateFormat.getTimeInstance(DateFormat.DEFAULT, Locale.GERMAN);

            String b = new StringBuilder(String.valueOf(jSpinner2.getValue())).substring(11, 19);

            while (!(k.format(new Date())).equals(b)) {
                ;
            }
            return null;
        }

        @Override
        public void done() {
            alarmDialog.setVisible(true);
        }
    }

