    public static void showDialog() {
        int response = JOptionPane.showConfirmDialog(frame, "Do you want to exit AlarmApp?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == 0) {
            close();
        } else {
            frame.setVisible(true);
        }
    }

